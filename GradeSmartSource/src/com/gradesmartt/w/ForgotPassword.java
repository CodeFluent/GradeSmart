    private void continueButtonOneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continueButtonOneActionPerformed
        // TODO add your handling code here:
        if(userInputOne.getText().equals("")){
            errorMessageLabelOne.setText("Enter an Username!");
            errorMessageLabelOne.setVisible(true);
        }
        else{
            if(UserList.users.containsKey(userInputOne.getText()) == false){
            errorMessageLabelOne.setText("Sorry user does not exist in the system!");
            errorMessageLabelOne.setVisible(true);
            }
            else{
            favHistPerson = UserList.users.get(userInputOne.getText()).getAnswerOne();
            favMovie = UserList.users.get(userInputOne.getText()).getAnswerTwo();
            favTeam = UserList.users.get(userInputOne.getText()).getAnswerThree();
            errorMessageLabelOne.setVisible(false);
            forgotPassPanel.setVisible(false);
            recoveryQuestionPanel.setVisible(true);
            }  
        }
    }//GEN-LAST:event_continueButtonOneActionPerformed

    private void continueButtonTwoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continueButtonTwoActionPerformed
        // TODO add your handling code here:
        
        if(favHistPersonInput.getText().equals(favHistPerson) == false){
            errorMessageLabelTwo.setText("Sorry your answer or answers do not match!");
            errorMessageLabelTwo.setVisible(true);
        }
        else if(favMovieInput.getText().equals(favMovie) == false){
            errorMessageLabelTwo.setText("Sorry your answer or answers do not match!");
            errorMessageLabelTwo.setVisible(true);
        }
        else if(favTeamInput.getText().equals(favTeam) == false){
            errorMessageLabelTwo.setText("Sorry your answer or answers do not match!");
            errorMessageLabelTwo.setVisible(true);
        }
        else{
            errorMessageLabelTwo.setVisible(false);
            recoveryQuestionPanel.setVisible(false);
            resetPassPanel.setVisible(true);
        }
    }//GEN-LAST:event_continueButtonTwoActionPerformed

