    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        // TODO add your handling code here:
        if(newPassInput.getText().equals("")){
                errorMessageLabelThree.setText("You need to put in a new password!");
                errorMessageLabelThree.setVisible(true);            
        }
        else{
            if(newPassInput.getText().equals(retypePassInput.getText()) == false){
                errorMessageLabelThree.setText("Sorry your passwords do not match!");
                errorMessageLabelThree.setVisible(true);
            }
            else{
                User tmpUser = UserList.users.get(userInputOne.getText());
                tmpUser.setPassword(newPassInput.getText());
                
                UserList.users.remove(userInputOne.getText());
                UserList.users.put(tmpUser.getUsername(),tmpUser);
                
                errorMessageLabelThree.setVisible(false);
                resetPassPanel.setVisible(false);
                passResetConfirmPanel.setVisible(true);
            }
        }
    }//GEN-LAST:event_confirmButtonActionPerformed

